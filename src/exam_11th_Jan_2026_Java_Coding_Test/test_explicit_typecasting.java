package exam_11th_Jan_2026_Java_Coding_Test;

public class test_explicit_typecasting {
    public static void main(String[] args) {
        double d = 10.75;   // double value
        int i = (int) d;    // explicit type casting

        System.out.println("Double value: " + d);
        System.out.println("Integer value after casting: " + i);
    }
}
